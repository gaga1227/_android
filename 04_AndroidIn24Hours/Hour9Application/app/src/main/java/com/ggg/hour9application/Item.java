package com.ggg.hour9application;

public class Item {
    String mName;
    String mDescription;
    double mPrice;

    /**
     * Constructor
     * @param name
     * @param description
     * @param price
     */
    public Item (String name, String description, double price){
        this.mName = name;
        this.mDescription = description;
        this.mPrice = price;
    }
}
